package exerclasse04;

public class ClasseFuncionario {

	public String nome;
	public String cpf;
	public String endereco;
	public String cep;
	
public String nomeFuncionario(String nome1) {
		nome = nome1;
		return nome;
	}
	
	public String cpfFuncionario(String cpf1) {
		cpf = cpf1;
		return cpf;
	}
	public String enderecoFuncionario(String endereco1){
		endereco = endereco1;
		return endereco;
	}
	public String cepFuncionario(String cep1) {
		cep = cep1;
		return cep;
	}
}
